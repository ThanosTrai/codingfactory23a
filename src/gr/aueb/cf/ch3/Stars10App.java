package gr.aueb.cf.ch3;

/**
 * Εκτυπώνει 10 αστεράκια (10 φορές)
 * από ένα αστεράκι).
 */
public class Stars10App {

    public static void main(String[] args) {

        int i = 1;

        while(i <=10) {
            System.out.println("*"); //αν πουμε σκετο print θα εκτυπώσει 10 αστεράκια στην σειρά
            i++;
        }
    }
}
